#!/usr/bin/env python3
"""
Palantir Dashboard — Daily Scraper
Collects new contract data and media mentions.

Routing architecture:
  contract_api items  → auto-merge directly into data.js (no approval)
  sec_edgar items     → Inbox for review
  high-score media    → Inbox (score >= 3 AND mentions Palantir)
  everything else     → KarpTube
"""
import json
import os
import re
import sys
from datetime import datetime

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
DASHBOARD_DIR = os.path.join(ROOT, "palantir-dashboard")
PENDING_JS_PATH = os.path.join(DASHBOARD_DIR, "pending.js")
KARPTUBE_JS_PATH = os.path.join(DASHBOARD_DIR, "karptube.js")
DATA_JS_PATH = os.path.join(DASHBOARD_DIR, "data.js")
SEEN_PATH = os.path.join(os.path.dirname(__file__), "seen_ids.json")

sys.path.insert(0, os.path.dirname(__file__))

from scrapers import (
    us_spending, uk_contracts, eu_ted, canada,
    australia, ukraine_prozorro, sec_edgar, palantir_ir,
    rss_feeds, web_search,
)
from screener import is_inbox_item
import source_registry


def load_seen_ids():
    if os.path.exists(SEEN_PATH):
        with open(SEEN_PATH, "r") as f:
            return set(json.load(f))
    return set()


def save_seen_ids(seen):
    with open(SEEN_PATH, "w") as f:
        json.dump(sorted(seen), f)


def load_existing_pending():
    if not os.path.exists(PENDING_JS_PATH):
        return []
    with open(PENDING_JS_PATH, "r", encoding="utf-8") as f:
        content = f.read()
    start = content.find("[")
    end = content.rfind("]") + 1
    if start == -1 or end == 0:
        return []
    try:
        return json.loads(content[start:end])
    except json.JSONDecodeError:
        return []


def load_existing_karptube():
    if not os.path.exists(KARPTUBE_JS_PATH):
        return []
    with open(KARPTUBE_JS_PATH, "r", encoding="utf-8") as f:
        content = f.read()
    start = content.find("[")
    end = content.rfind("]") + 1
    if start == -1 or end == 0:
        return []
    try:
        return json.loads(content[start:end])
    except json.JSONDecodeError:
        return []


def write_karptube_js(items):
    ts = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ")
    json_str = json.dumps(items, indent=2, ensure_ascii=False)
    content = f"// Auto-generated by scraper. Last updated: {ts}\nwindow.KARPTUBE_ITEMS = {json_str};\n"
    with open(KARPTUBE_JS_PATH, "w", encoding="utf-8") as f:
        f.write(content)


def write_pending_js(items):
    # Only keep items that have Palantir/PLTR mention OR are sec_edgar (auto-pass)
    clean = []
    for item in items:
        src_type = item.get("source_type", "")
        if src_type == "sec_edgar":
            clean.append(item)
            continue
        text = (item.get("title", "") + " " + item.get("snippet", "")).lower()
        if "palantir" in text or "pltr" in text:
            # Also require a screen_score (discard pre-screener legacy items)
            if "screen_score" in item:
                clean.append(item)

    ts = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ")
    json_str = json.dumps(clean, indent=2, ensure_ascii=False)
    content = f"// Auto-generated by scraper. Last updated: {ts}\nwindow.PENDING_ITEMS = {json_str};\n"
    with open(PENDING_JS_PATH, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"\n[main] Written {len(clean)} items to pending.js (filtered from {len(items)})")


# ---------------------------------------------------------------------------
# Auto-merge contract API items directly into data.js
# ---------------------------------------------------------------------------

def _get_max_contract_id():
    """Read data.js and return the current maximum contract ID."""
    try:
        with open(DATA_JS_PATH, "r", encoding="utf-8") as f:
            content = f.read()
        ids = [int(x) for x in re.findall(r"\{ id: (\d+),", content)]
        return max(ids) if ids else 462
    except Exception:
        return 462


def _item_to_contract_js(item, contract_id):
    """Format a contract_api scraper item as a data.js entry."""
    cd = item.get("contract_data") or {}
    title = item.get("title", "Palantir Contract")[:120]
    entity = cd.get("entity") or "US Government"
    country = cd.get("country") or "United States"
    value = cd.get("value") or 0
    year = cd.get("year") or int((item.get("date") or "2025-01-01")[:4])
    url = item.get("url", "")
    source_name = item.get("source", "USASpending.gov")
    date_str = item.get("date") or "2025-01-01"

    # Derived fields
    region_map = {
        "United States": "North America", "Canada": "North America",
        "United Kingdom": "Europe", "Germany": "Europe", "France": "Europe",
        "Norway": "Europe", "Denmark": "Europe", "Netherlands": "Europe",
        "Poland": "Europe", "EU/NATO": "Europe",
        "Australia": "Asia-Pacific", "New Zealand": "Asia-Pacific",
        "Japan": "Asia-Pacific",
        "Israel": "Middle East",
        "Ukraine": "Europe",
    }
    region = region_map.get(country, "Other")

    defense_keywords = [
        "army", "navy", "air force", "marine", "pentagon", "defense",
        "socom", "jsoc", "centcom", "darpa", "intelligence", "missile",
        "disa", "nro", "nga", "dia",
    ]
    entity_lower = entity.lower()
    sector = "Defense" if any(k in entity_lower for k in defense_keywords) else "Government"

    try:
        month = int(date_str[5:7])
    except (ValueError, IndexError):
        month = 1
    quarter = f"Q{(month - 1) // 3 + 1}"

    # Build JS object string (matches data.js formatting style)
    js = (
        f"  {{ id: {contract_id}, "
        f"name: {json.dumps(title)}, "
        f"entity: {json.dumps(entity)}, "
        f"country: {json.dumps(country)}, "
        f"region: {json.dumps(region)}, "
        f"sector: {json.dumps(sector)}, "
        f"sub: \"Data Analytics / AI\", "
        f"product: \"Palantir\", "
        f"value: {value:.2f}, "
        f"currency: \"USD\", "
        f"year: {year}, "
        f"quarter: {json.dumps(quarter)}, "
        f"status: \"Active\", "
        f"statusDetail: \"\", "
        f"procurement: \"Federal Contract\", "
        f"source: {json.dumps(source_name)}, "
        f"url: {json.dumps(url)}, "
        f"docs: [\n"
        f"    {{ label: {json.dumps(source_name)}, url: {json.dumps(url)}, type: \"fed_record\" }},\n"
        f"  ]}}"
    )
    return js


def auto_merge_contracts(contract_items):
    """Append new contract_api items directly to data.js — no approval needed."""
    if not contract_items:
        return 0

    try:
        with open(DATA_JS_PATH, "r", encoding="utf-8") as f:
            content = f.read()
    except Exception as e:
        print(f"[main] auto_merge_contracts: could not read data.js: {e}")
        return 0

    max_id = _get_max_contract_id()

    # Find the insertion point — just before the closing ]; of window.CONTRACTS
    # The CONTRACTS array ends before window.COLORS
    colors_start = content.find("\nwindow.COLORS")
    if colors_start == -1:
        # Fallback: find last ];
        insert_at = content.rfind("\n];")
    else:
        # Find the ];\n just before window.COLORS
        insert_at = content.rfind("\n];", 0, colors_start)

    if insert_at == -1:
        print("[main] auto_merge_contracts: could not find insertion point in data.js")
        return 0

    new_entries = []
    for i, item in enumerate(contract_items):
        cid = max_id + i + 1
        js_obj = _item_to_contract_js(item, cid)
        new_entries.append(js_obj)

    insert_text = ",\n" + ",\n".join(new_entries)
    new_content = content[:insert_at] + insert_text + content[insert_at:]

    try:
        with open(DATA_JS_PATH, "w", encoding="utf-8") as f:
            f.write(new_content)
        print(f"[main] auto_merge_contracts: appended {len(new_entries)} contracts to data.js")
        return len(new_entries)
    except Exception as e:
        print(f"[main] auto_merge_contracts: write failed: {e}")
        return 0


# ---------------------------------------------------------------------------
# Main pipeline
# ---------------------------------------------------------------------------

def run():
    print(f"[main] Starting scraper run at {datetime.utcnow().isoformat()}Z")
    seen_ids = load_seen_ids()

    all_items = []
    contract_api_items = []  # separated for auto-merge

    # --- Tier 1: Contract APIs ---
    print("\n=== Contract APIs ===")
    for scraper_fn in [us_spending.scrape, uk_contracts.scrape, eu_ted.scrape,
                       canada.scrape, australia.scrape, ukraine_prozorro.scrape]:
        try:
            items = scraper_fn()
            contract_api_items.extend(items)
            all_items.extend(items)
        except Exception as e:
            print(f"[main] Scraper {scraper_fn.__module__} failed: {e}")

    # --- Tier 2: Official disclosures ---
    print("\n=== Official Disclosures ===")
    for scraper_fn in [sec_edgar.scrape, palantir_ir.scrape]:
        try:
            all_items.extend(scraper_fn())
        except Exception as e:
            print(f"[main] Scraper {scraper_fn.__module__} failed: {e}")

    # --- Tier 3: RSS feeds ---
    print("\n=== RSS Feeds ===")
    try:
        screened_rss = source_registry.get_rss_sources(destinations=["inbox", "screened"])
        all_items.extend(rss_feeds.scrape(screened_rss))
    except Exception as e:
        print(f"[main] RSS scraper failed: {e}")

    # --- Tier 3: Web/X search ---
    print("\n=== Web / X Search ===")
    try:
        screened_x = source_registry.get_x_handles(destinations=["inbox", "screened"])
        all_items.extend(web_search.scrape(screened_x))
    except Exception as e:
        print(f"[main] Web search scraper failed: {e}")

    # --- Auto-merge contract API items directly to data.js ---
    print("\n=== Auto-Merge Contracts ===")
    new_contract_items = [
        item for item in contract_api_items
        if item.get("id") and item["id"] not in seen_ids
        and item.get("contract_data")  # must have structured data
    ]
    merged_count = auto_merge_contracts(new_contract_items)
    # Mark these as seen so they don't re-appear
    for item in new_contract_items:
        seen_ids.add(item["id"])

    # --- Screen & route remaining items ---
    print("\n=== Screening & Routing ===")

    existing_pending = load_existing_pending()
    existing_pending_ids = {item["id"] for item in existing_pending}

    existing_karptube = load_existing_karptube()
    existing_karptube_ids = {item["id"] for item in existing_karptube}

    new_inbox = []
    new_karptube = []

    for item in all_items:
        if not item.get("id") or not item.get("url"):
            continue
        if item["id"] in seen_ids:
            continue
        # Skip contract_api — already handled above
        if item.get("source_type") == "contract_api":
            continue

        should_inbox, score, reason = is_inbox_item(item)
        item["screen_score"] = score
        item["screen_reason"] = reason
        seen_ids.add(item["id"])

        # Circle 1: KarpTube (everything)
        if item["id"] not in existing_karptube_ids:
            new_karptube.append(item)
            existing_karptube_ids.add(item["id"])

        # Circle 2: Inbox (only genuine Palantir deal signals)
        if should_inbox:
            if item["id"] not in existing_pending_ids:
                new_inbox.append(item)

    print(f"[main] {merged_count} contracts auto-merged to data.js")
    print(f"[main] {len(new_karptube)} -> KarpTube, {len(new_inbox)} -> Inbox")

    # Write pending.js — filtered to Palantir-only, screened items
    # (write_pending_js applies filtering; merged_pending may be larger before filter)
    merged_pending_raw = (new_inbox + existing_pending)[:500]
    write_pending_js(merged_pending_raw)  # filter + write; prints actual count

    # Write karptube.js
    merged_karptube = new_karptube + existing_karptube
    merged_karptube.sort(
        key=lambda x: x.get("date", "") or x.get("scraped_at", ""),
        reverse=True,
    )
    merged_karptube = merged_karptube[:1000]
    write_karptube_js(merged_karptube)
    print(f"[main] KarpTube: {len(merged_karptube)} total ({len(new_karptube)} new)")

    save_seen_ids(seen_ids)
    source_registry.save()

    print(f"[main] Done. Contracts auto-merged: {merged_count} | KarpTube: {len(merged_karptube)}")


if __name__ == "__main__":
    run()
